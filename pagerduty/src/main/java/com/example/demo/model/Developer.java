package com.example.demo.model;

public class Developer {
    private int id;
    private int teamID;
    private String name;
    private String phone;

    public Developer(int id, int teamID, String name, String phone) {
        this.id = id;
        this.teamID = teamID;
        this.name = name;
        this.phone = phone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTeamID() {
        return teamID;
    }

    public void setTeamID(int teamID) {
        this.teamID = teamID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
